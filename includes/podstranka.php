	<h2>Bezpečnostný projekt na ochranu osobných údajov</h2>
	<h3>Úvod k bezpečnostným projektom podľa osobitných predpisov.</h3>
	<p>
	Legislatíva SR venuje osobitnú pozornosť dôležitosti určitých typov údajov. Ide o osobné údaje a údaje spadajúce do kategórie utajovaných skutočností. Pre tieto typy údajov zákony predpisujú vypracovanie špecifických Bezpečnostných projektov (Zákon č. 215/2004 Z. z. o ochrane utajovaných skutočností a Zákon č. 428/2002 Z. z. o ochrane osobných údajov). Pri riešení tohto typu Bezpečnostných projektov sa základné postupy našej metodiky nemenia, avšak v niektorých detailoch sú prispôsobené požiadavkám spomenutých zákonov.
	Nikto nie je doma prorokom. Aj keď organizácia má vlastných odborníkov, vypracovanie Bezpečnostného projektu sa zvyčajne zadáva externej firme.
	Poskytneme Vám pohľad zvonku a prispejeme novými podnetmi, vychádzajúcimi zo znalosti bezpečnostných technológii a reálnych problémov našich zákazníkov. Každý Bezpečnostný projekt je „šitý na mieru,“ 
	je vyvážený a obsahuje špecifikáciu bezpeč-nostných cieľov a kombinovaných opatrení podľa reálnych potrieb.
	</p>
	
	<h3>Bezpečnostný projekt na ochranu osobných údajov.</h3>
	<p>
	Zákon č. 428/2002 Z. z. o ochrane osobných údajov v znení neskorších predpisov okrem iného upravuje ochranu osobných údajov fyzických osôb pri ich spracúvaní, zásady spracúvania osobných údajov 
	a ich bezpečnosť.
	Tento zákon sa vzťahuje na osobné údaje systematicky spracúvané - úplne alebo čiastočne automatizovanými prostriedkami spracúvania, alebo inými ako automatizo-vanými prostriedkami spracúvania, ktoré sú súčasťou informačného systému, alebo sú určené na spracúvanie v IS.
	Informačným systémom je akýkoľvek usporiadaný súbor, sústava, alebo databáza, obsa-hujúca jeden, 
	alebo viac osobných údajov, ktoré sú systematicky spracúvané za použitia automatizovaných, alebo iných 
	ako automatizovaných prostriedkov spracúvania (napríklad  kartotéka, zoznam, register, operát, záznam alebo sústava obsahujúca spisy, a pod.). 
	Za bezpečnosť osobných údajov zodpovedá prevádzkovateľ a sprostredkovateľ prijatím primeraných technických, organizačných a personálnych opatrení, ktoré zodpovedajú spôsobu spracúvania osobných údajov. Práve tieto opatrenia sa prijímajú vo forme Bezpečnostného projektu informačného systému.
	Bezpečnostný projekt vymedzuje rozsah všetkých opatrení potrebných na eliminovanie a minimalizovanie hrozieb a rizík, pôsobiacich na informačný systém z hľadiska narušenia jeho bezpečnosti, spoľahlivosti 
	a funkčnosti. Obsahuje najmä bezpečnostný zámer, analýzu bezpečnosti informačného systému 
	a bezpečnostné smernice.
	</p>
	
